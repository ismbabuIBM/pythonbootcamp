#if..elif..else statements

whrToGo = 'Staples'

if whrToGo == 'Grocery':
    print('Welcome to the Grocery store')
elif whrToGo == 'Staples':
    print('Welcome to the Staples store')
elif whrToGo == 'Veggies':
    print('Welcome to the Veggies store')
elif whrToGo == 'Automotive':
    print('Welcome to the Automotive store')    
else:
    print('You are at the wrong place.')